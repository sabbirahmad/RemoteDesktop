    private void settingsOKButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_settingsOKButtonActionPerformed
         // TODO add your handling code here:
        try { //writing alias (changed/previously set) to file
            writeFile= new BufferedWriter(new FileWriter("alias.log"));
            writeFile.write(aliasField.getText());//writes from text field
            alias=aliasField.getText();//sets new alias if changed
            writeFile.close();
        }//end try 
        catch (IOException ex) {
            Logger.getLogger(RemoteDesktop.class.getName()).log(Level.SEVERE, null, ex);
        }//end catch

        try {//writes file dor checkbox check
            writeFile= new BufferedWriter(new FileWriter("exitDialogue.log"));
            if(exitDialogueBox.isSelected())
            {
                exitShow=true;
                writeFile.write("1");//writes 1 for selected
            }
            else
            {
                exitShow=false;
                writeFile.write("0");//writes 0 for deselected
            }
            writeFile.close();
        }//end try
        catch (IOException ex) {
            Logger.getLogger(RemoteDesktop.class.getName()).log(Level.SEVERE, null, ex);
        }//end catch
        
        try {//write white list contacts
            writeFile= new BufferedWriter(new FileWriter("whitelist.log"));
            writeFile.write(whiteListTextArea.getText());
            writeFile.close();
        }//end try
        catch (IOException ex) {
            Logger.getLogger(RemoteDesktop.class.getName()).log(Level.SEVERE, null, ex);
        }//end catch
        
        
         try {//write black list contacts
            writeFile= new BufferedWriter(new FileWriter("blacklist.log"));
            writeFile.write(blackListTextArea.getText());
            writeFile.close();
        }//end try
        catch (IOException ex) {
            Logger.getLogger(RemoteDesktop.class.getName()).log(Level.SEVERE, null, ex);
        }//end catch
        
        settingsFrame.setVisible(false);//settings frame hide
    }//GEN-LAST:event_settingsOKButtonActionPerformed

