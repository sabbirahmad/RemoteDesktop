    private void settingsItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_settingsItemActionPerformed
        // TODO add your handling code here:
        openFile("alias.log");//open file to read alias
        alias=file.next();//read string for alias
        closeFile();//closes file
        
        openFile("exitDialogue.log");//open file to check exit box condition
        showExitDialogue=file.nextInt();//read int value 1-for show, 0-for hide
        closeFile();//closes file
        
        whiteListTextArea.setText(null);//clears the text area
        openFile("whitelist.log");//read whitelist
        while(file.hasNext())
        {
            whiteListTextArea.append(file.next()+"\n");//white list contacts appending to textarea
        }//end while
        closeFile();//closes file
        
        blackListTextArea.setText(null);//clears text area
        openFile("blacklist.log");//read black list
        while(file.hasNext())
        {
            blackListTextArea.append(file.next()+"\n");//black list contacts appending to textarea
        }
        closeFile();
        
        aliasField.setText(alias);//shows alias
        if(showExitDialogue==1)//check to show checkbox enabled or not
        {
            exitDialogueBox.setSelected(true);//sets checkbox selected
        }
        else
        {
            exitDialogueBox.setSelected(false);//sets checkbox diselected   
        }
        
        settingsFrame.pack();
        settingsFrame.setVisible(true);//show settings frame
    }//GEN-LAST:event_settingsItemActionPerformed

